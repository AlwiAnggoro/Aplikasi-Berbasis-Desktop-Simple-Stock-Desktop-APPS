    private void tampilData(){
        //untuk mengahapus baris setelah input
        int row = tb_barang.getRowCount();
        for(int a = 0 ; a < row ; a++){
            table.removeRow(0);
        }
        
        String query = "SELECT * FROM `tb_stokbarang` ORDER BY namabarang";
        
        try{
            Connection connect = koneksi.getKoneksi();//memanggil koneksi
            Statement sttmnt = connect.createStatement();//membuat statement
            ResultSet rslt = sttmnt.executeQuery(query);//menjalanakn query
            
            while (rslt.next()){
                //menampung data sementara
                   
                    String id = rslt.getString("idbarang");
                    String nama = rslt.getString("namabarang");
                    String harga = rslt.getString("harga");
                    String stok = rslt.getString("stokbarang");
                    
                //masukan semua data kedalam array
                String[] data = {id,nama,harga,stok};
                //menambahakan baris sesuai dengan data yang tersimpan diarray
                table.addRow(data);
            }
                //mengeset nilai yang ditampung agar muncul di table
                tb_barang.setModel(table);
            
        }catch(Exception e){
            System.out.println(e);
        }
       
    }
