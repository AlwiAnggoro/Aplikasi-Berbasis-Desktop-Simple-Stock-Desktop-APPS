    private void tb_barangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_barangMouseClicked
        // TODO add your handling code here:
        int row = tb_barang.getSelectedRow();
        transaksi home = new transaksi();
        
        String kode = table.getValueAt(row, 0).toString();
        home.txt_idbarang.setText(kode);
        
        String nama = table.getValueAt(row, 1).toString();
        home.txt_namabarang.setText(nama);
        
        String harga = table.getValueAt(row, 2).toString();
        home.txt_harga.setText(harga);
        
        home.setVisible(true);
        home.pack();
        home.setDefaultCloseOperation(transaksi.DISPOSE_ON_CLOSE);
        dispose();
    }//GEN-LAST:event_tb_barangMouseClicked

